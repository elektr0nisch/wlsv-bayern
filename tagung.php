<h1>Bundesweite W-LSV Klausurtagung</h1>

<p><a href="/">&larr; Zurück</a></p>

<p>
    Liebe Schülerinnen und Schüler,
    wir laden herzlich zur W-LSV Klausurtagung, 27.5.-29.5.2023 in die Freie Waldorfschule Augsburg
    ein. Es kommen Waldorfschüler:innen aus ganz Deutschland, um gemeinsame Inhalte zu
    erarbeiten, selber Neues zu lernen, sich mit anderen Waldis auszutauschen und gemeinsam
    eine schöne Zeit zu verbringen. Gestaltet durch spannende Vorträge, selbstgewählte
    Workshops, singen, tanzen, Kreatives, thematische Arbeitsgruppen.... sodass für jeden etwas
    dabei ist.
</p>

<h2>Thema der Tagung</h2>

<ul>
    <li>
        Der Aufbau von Waldorf-Landes-Schüler:innen-Vertretungen (W-LSV)
        <ul>
            <li>Wir beschäftigen uns damit, wie wir in jedem Bundesland ein stabiles Netzwerk unter
                Waldorfschülern aufbauen, welche sich austauschen und gemeinsame Themen und
                Projekte bearbeiten
            </li>
            <li>
                NEU: Schülervertretung in Bayern (W-LSV): Aufbau und Organisation, Teambuilding,
                Austausch und Entwurf einer bayernweiten Schülerzeitung, kreative Anregungen
                willkommen.
            </li>
        </ul>
    </li>
    <li>
        Verbesserung von schulischen SMV-Strukturen
        <ul>
            <li>
                Wir erarbeiten Methoden, wie eine Schülermitverwaltung produktiv
                zusammenarbeiten kann, bzw. wie man eine aktive SMV aufbaut.
            </li>
        </ul>
    </li>
</ul>

